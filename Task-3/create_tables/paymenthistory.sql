CREATE TABLE PaymentHistory(
    ID SERIAL PRIMARY KEY,
    Credit INTEGER NOT NULL REFERENCES issuedcredits ON DELETE RESTRICT,
    PaymentType INTEGER NOT NULL REFERENCES paymenttypes ON DELETE RESTRICT,
    Date DATE NOT NULL,
    SumPercent INTEGER NOT NULL,
    SumLoan INTEGER NOT NULL,
    Penalty INTEGER
);

